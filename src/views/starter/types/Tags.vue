<script>
import {
    mapGetters
} from 'vuex';

import {
    validationMixin
} from 'vuelidate'

import {
    required,
    minLength
} from 'vuelidate/lib/validators'

export default {
    mixins: [validationMixin],

    data() {
        return {
            vSelectOptionsMultipleSelected: null,
            logo: null,
            form: {
                label: '',
                companies_id: ''
            },
            companiesArray: []
        }
    },
    validations: {
        form: {
            label: {
                required,
                minLength: minLength(3)
            },
            // companies_id: {
